package org.example.Array;

public class ArrayUsingStrings {
    public static void main(String[] args) {
        String s[] = new String[4];
        s[0] = "Chintu";
        s[1] = "I";
        s[2] = "love";
        s[3] = "You";
        for (int i = 0; i < 4; i++) {
            System.out.println(s[i]);

        }
    }
}
